/**
 * explicit_typecast
 */
public class explicit_typecast {
    public static void main(String[] args) {
        //explicit type casting 
        double d = 112.23;
        int i = (int) d; // double is explicitly cast to int
        System.out.println("double d="+d);
        System.out.println("int i="+i);
    }
    
}