public class literal {
    public static void main(String[] args) {
    int intval = 42;
    double doubleval = 3.14;
    char charVal = 'A';
    String stringval = "Hello";
    boolean boolVal = true;
    float floatval=12.10f;
    System.out.println("intvalue="+intval);
    System.out.println("floatvalue="+floatval);
    System.out.println("doublevalue="+doubleval);
    System.out.println("charvalue="+charVal);
    System.out.println("stringvalue="+stringval);
    System.out.println("boolvalue="+boolVal);
    }
   
}

