public class assignment_op {
     public static void main(String[] args) {

         // Assignment Operator (=)
        int n1 = 100; // n1 is assigned the value 100

        // Addition Assignment (+=)
        int n2 = 50;
        n1 += 10; // Equivalent to n1 = n1 + 50;

        // Subtraction Assignment (-=)
        int n3 = 70;
        n3 -= 5; // Equivalent to n3 = n3 - 5;

        // Multiplication Assignment (*=)
        int n4 = 20;
        n4 *= 2; // Equivalent to n4 = n4 * 2;

        // Division Assignment (/=)
        int n5 = 80;
        n5 /= 2; // Equivalent to n5 = n5 / 2;

        // Modulus Assignment (%=)
        int n6 = 120;
        n6 %= 40; // Equivalent to n6 = n6 % 40;

        // Displaying the results
        System.out.println("n1= " + n1);
        System.out.println("n2= " + n2);
        System.out.println("n3= " + n3);
        System.out.println("n4= " + n4);
        System.out.println("n5= " + n5);
        System.out.println("n6= " + n6);        
    }
}  
